    <h2 style="">The New Kid at School</h2>
    <h3> The Situation</h3>
      <h4>Henry was a shy sixth-grader who recently arrived at school from out of state. One day as he was browsing a social media site he came across a page about the school with pictures of students, including one of him labeled “The Fat Nerd.” Upset, he posted a reply expressing his dismay. </h4>
      <h4>The postings became nastier, and soon some students were making loud comments in the lunchroom and on the playground. A teacher overhead some of the names, and asked Henry what was going on. He described the social media page and the cyberbullying.</h4><br>
      <h3>What Worked</h3>
      <h4>The school, which had trained its teachers and had a zero-tolerance policy on bullying, responded quickly. They identified the ringleaders, and with the help of their parents, had the creator of the unauthorized school page take it down from the social media site. School officials hosted sessions for parents and students about cyberbullying and how to prevent it.</h4><br>
      <h3>What Didn’t Work</h3>
      <h4>Henry had to endure many weeks of feeling humiliated and hurt before anyone realized what was going on. His parents didn’t even know because Henry was too embarrassed to tell them. It will take the still-shy teen a long time to regain his confidence.</h4><br>
      <h3>Lessons</h3>
      <ul>
      <li><h4>Teach your kids early about acceptable wireless behavior, including how to identify and avoid bullying—online and off.</h4></li>
      <li><h4>Encourage your kids to tell you if someone texts or posts harmful or inappropriate messages or pictures about them. You may want to include this as part of your family’s rules.</h4></li>
      <li><h4>Learn about the wireless policies of your child’s school district and discuss them as a family.</h4></li>
      <li><h4>Learn about the state and/or federal laws that may help protect your kids and discuss them as a family.</h4></li>
      <li><h4>Use parental control tools to manage or monitor your kids activities, including social media profiles.</h4></li>
    </ul>
