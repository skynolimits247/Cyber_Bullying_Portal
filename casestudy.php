    <h2>Hateful Texting</h2>
    <h3> The Situation</h3>
    <h4>Gina, a exuberant and spirited high school freshman, had a falling out with a girlfriend over a boy they both liked at school. It wasn’t long before Gina began getting rude text messages to her phone at all hours, calling her names and saying inappropriate things such as, “I hope you die soon.” The angry friend had enlisted others to text hateful remarks to Gina too.</h4>

<h4>Gina was devastated, and soon began to withdraw from her friends and active social life. She spent more and more time alone in her room. Her mom grew concerned when Gina would no longer talk about what was going on at school or in her life. So she reached out to one of Gina’s closest friends, and learned about the hateful texts her daughter was receiving.</h4>
<br>
<h3>What Worked</h3>
      <h4>Gina’s mom took action before the emotional toll became worse. She told Gina she knew about the cyberbullying, and together, they discussed the situation with the school counselor and principal. With support from her family and teachers she trusted, Gina found the strength to talk to the girlfriend she had argued with, apologized for the quarrel and asked her to call off the texting campaign. While their friendship was over, the cyberbullying stopped.</h4><br>
      <h3>What Didn’t Work</h3>
      <h4>Gina’s parents had never talked about bullying, or how to handle such behavior when you become the victim. The 14-year-old was completely unprepared to face such a major assault on her self-esteem.</h4><br>
      <h3>Lessons</h3>
      <ul>
      <li><h4>Create family rules that equip your kids with information on how to identify bullying behaviors, and encourage them to tell you if they feel they are being bullied. This includes your child’s friends to be able to share with you if they are being bullied.</h4></li>
      <li><h4>Teach your kids about acceptable online behavior, including how bullying can turn into vicious cyberbullying.</h4></li>
      <li><h4>Use parental controls to block phone numbers that are the source of inappropriate texts.</h4></li>
    </ul><br><br><br>
